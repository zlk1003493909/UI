//生成三角波的函数
int16_t Triangle_Func(int16_t x)
{
    static int16_t ret = 0;
    if(x%20 != 0) ret+= (curve_option_array[1].item_max - curve_option_array[1].item_min)/2/19;
    else ret = curve_option_array[1].item_min/2 ; 
    return ret;
}

